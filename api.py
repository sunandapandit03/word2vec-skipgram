@app.post("/predict", response_model=PredictionResponse)
async def predict(file: UploadFile = File(...)):
    """
    POST /predict
    =============
    Receives a bridge image and returns a full analysis.

    REQUEST:
      Content-Type: multipart/form-data
      Field name: "file"
      File: JPEG, PNG, or WEBP image

    RESPONSE: PredictionResponse JSON (see schema above)

    HOW YOUR BACKEND PERSON CALLS THIS:
      const form = new FormData();
      form.append('file', imageBuffer, filename);
      const result = await axios.post('http://ml_api:8000/predict', form);
    """
    # Validate that the uploaded file is actually an image
    if file.content_type not in ("image/jpeg", "image/png", "image/webp"):
        raise HTTPException(
            status_code=400,
            detail=f"Unsupported file type: {file.content_type}. Use JPEG, PNG, or WEBP."
        )

    # Read image bytes and convert to PIL Image
    contents = await file.read()
    try:
        pil_image = Image.open(io.BytesIO(contents)).convert("RGB")
    except Exception:
        raise HTTPException(status_code=400, detail="Could not open image file. Is it corrupted?")

    # Preprocess: resize → normalize → add batch dimension
    # unsqueeze(0) adds a batch dimension: (3, 300, 300) → (1, 3, 300, 300)
    # The model expects batches, even if we're only processing 1 image.
    tensor = VAL_TRANSFORMS(pil_image).unsqueeze(0).to(DEVICE)

    # Run the model — no gradient computation needed at inference time
    with torch.no_grad():
        outputs = model(tensor)

    # ── Parse Head 1: Crack detection ────────────────────────────────────────
    # softmax converts raw logits to probabilities that sum to 1.0
    # e.g. [0.2, 3.8] → [0.02, 0.98] (98% cracked)
    damage_probs = torch.softmax(outputs["damage_logits"], dim=1)[0]
    damage_idx   = damage_probs.argmax().item()
    cracked      = (damage_idx == 1)
    confidence   = damage_probs[damage_idx].item()

    # ── Parse Head 2: Structure type ─────────────────────────────────────────
    struct_probs = torch.softmax(outputs["structure_logits"], dim=1)[0]
    struct_idx   = struct_probs.argmax().item()
    structure    = STRUCTURE_CLASSES[struct_idx]

    # ── Parse Head 3: Severity score ─────────────────────────────────────────
    severity = round(outputs["severity"][0, 0].item(), 1)
    severity = max(0.0, min(100.0, severity))   # clamp to [0, 100]

    # ── Parse Head 4: Days to failure ────────────────────────────────────────
    # The model outputs log(days+1). We reverse with expm1 = e^x - 1.
    log_val = outputs["timeline_log"][0, 0].item()
    days    = max(1, round(math.expm1(log_val)))

    # ── Fuse into risk level ──────────────────────────────────────────────────
    risk = fuse_risk(cracked, severity, days)

    # ── Generate Claude report ────────────────────────────────────────────────
    report_dict = generate_report(cracked, structure, severity, days, risk)

    # ── Build and return response ─────────────────────────────────────────────
    return PredictionResponse(
        damage_detected     = cracked,
        damage_confidence   = round(confidence, 4),
        structure_type      = structure,
        severity_score      = severity,
        days_to_failure     = days,
        risk_level          = risk,
        class_probabilities = {
            DAMAGE_CLASSES[i]: round(damage_probs[i].item(), 4)
            for i in range(len(DAMAGE_CLASSES))
        },
        report = ReportSection(**report_dict),
    )
